def expandContractions(text, c_re=c_re):
    def replace(match):
        return cList[match.group(0)]
    return c_re.sub(replace, text)


def correct_spelling(text): #string, list of words to not change
    temp = []
    for word in text.split(' '):
        if (0.985 <= Word(word).spellcheck()[0][1] < 1.0):
            w = Word(word).spellcheck()[0][0]
            temp.append(w)
        else:
            temp.append(word)
            
    return " ".join(temp).lower()


